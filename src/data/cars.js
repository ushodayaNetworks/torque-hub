// Function to get car by ID
export const getCarById = (id) => {
  return cars.find(car => car.id === parseInt(id));
};

// Function to filter cars based on criteria
export const filterCars = (filters) => {
  return cars.filter(car => {
    // Check if price is within range
    if (filters.minPrice && car.price < filters.minPrice) return false;
    if (filters.maxPrice && car.price > filters.maxPrice) return false;
    
    // Check make if specified
    if (filters.make && filters.make !== "All" && car.make !== filters.make) return false;
    
    // Check year if specified
    if (filters.minYear && car.year < filters.minYear) return false;
    if (filters.maxYear && car.year > filters.maxYear) return false;
    
    // Check type if specified
    if (filters.type && filters.type !== "All" && car.type !== filters.type) return false;
    
    return true;
  });
};

// Get unique values for filtering
export const getUniqueValues = (key) => {
  return [...new Set(cars.map(car => car[key]))];
};